/*ASCII 码值的范围如下：

### 1. **标准 ASCII 码**
   - **范围**：0 到 127（十进制）
   - **二进制表示**：7 位二进制数（0000000 到 1111111）
   - **字符数量**：128 个字符
     - **0 到 31**：控制字符（如换行、回车、制表符等）
     - **32 到 126**：可打印字符（包括空格、数字、大小写字母、标点符号等）
     - **127**：删除字符（DEL）

### 2. **扩展 ASCII 码**
   - **范围**：128 到 255（十进制）
   - **二进制表示**：8 位二进制数（10000000 到 11111111）
   - **字符数量**：128 个字符
     - 扩展 ASCII 码用于表示特殊符号、外来字母、图形字符等，但其具体字符定义可能因系统、语言或编码标准而异（如 ISO-8859-1、Windows-1252 等）。

### 总结：
- **标准 ASCII**：0–127（共 128 个字符）
- **扩展 ASCII**：0–255（共 256 个字符，包含标准 ASCII）

如果需要进一步了解特定字符的 ASCII 码值，请随时提问！*/

#include <stdio.h>

int main()
{
    int i=0;
    for(i=32;i<=126;i++)
    {
        printf("%c  ",i);
    }//打印所有的可打印字符。
    return 0;
}